import re
from collections import defaultdict


def solution(word, pages):
    base_scores = defaultdict(int)
    linked_urls = defaultdict(str)
    for page in pages:
        page_url = re.findall('<meta property="og:url" content="https://(.*?)"/>', page)[0]
        base_scores[page_url] = re.sub("[^a-z]", ".", page.lower()).split('.').count(word.lower())
        linked_urls[page_url] = re.findall('<a href="https://(.*?)">', page)

    link_scores = defaultdict(float)
    for page, linked_pages in linked_urls.items():
        if linked_pages:
            link_scores[page] += base_scores[page] / len(linked_pages)

    matching_scores = base_scores.copy()
    for page, linked_pages in linked_urls.items():
        for linked_page in linked_pages:
            matching_scores[linked_page] += link_scores[page]
            
    answer = sorted(enumerate(matching_scores.items()), key=lambda x: (-x[1][1], x[0]))[0][0]
    return answer


